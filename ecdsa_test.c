/**
 * @brief ECDSA test suite
 **/

error_t ecdsaTest(void)
{
    error_t error;
    uint_t i;
    uint_t n;
    uint8_t digest[64];
    EcDomainParameters params;
    EcPrivateKey refPrivateKey;
    EcPrivateKey calcPrivateKey;
    EcPublicKey refPublicKey;
    EcPublicKey calcPublicKey;
    EcdsaSignature refSignature;
    EcdsaSignature calcSignature;
    Mpi r;
    Mpi t;
    PrngSimulatorContext prngContext;
    static uint8_t random[128];

    TRACE_INFO("**********************\r\n");
    TRACE_INFO("** ECDSA Test Suite **\r\n");
    TRACE_INFO("**********************\r\n");
    TRACE_INFO("\r\n");

    error = NO_ERROR;

    //Initialize EC domain parameters
    ecInitDomainParameters(&params);

    //Initialize ECDSA private key (reference)
    ecInitPrivateKey(&refPrivateKey);
    //Initialize ECDSA private key
    ecInitPrivateKey(&calcPrivateKey);
    //Initialize ECDSA public key (reference)
    ecInitPublicKey(&calcPublicKey);
    //Initialize ECDSA public key
    ecInitPublicKey(&refPublicKey);
    //Initialize ECDSA signature (reference)
    ecdsaInitSignature(&refSignature);
    //Initialize ECDSA signature (calculated)
    ecdsaInitSignature(&calcSignature);

    //Initialize multiple precision integers
    mpiInit(&r);
    mpiInit(&t);

    //Loop through test vectors
    for(i = 0; i < arraysize(ecdsaTestVectors); i++)
    {
        //Point to the current test vector
        const EcdsaTestVector *tv = &ecdsaTestVectors[i];

        //Display test name
        TRACE_INFO("Test case #%d: %s\r\n", i + 1, tv->testLabel);

        //Debug message
        //TRACE_DEBUG("Loading %s EC domain parameters...\r\n", tv->params->name);
        //Load EC domain parameters
        EC_CHECK(ecLoadDomainParameters(&params, tv->params));

        //Read ECDSA private key
        MPI_CHECK(mpiReadRaw(&refPrivateKey.d, tv->x, tv->xLen));

        //Read ECDSA public key
        MPI_CHECK(mpiReadRaw(&refPublicKey.q.x, tv->qx, tv->qxLen));
        MPI_CHECK(mpiReadRaw(&refPublicKey.q.y, tv->qy, tv->qyLen));

        //Read ECDSA signature
        MPI_CHECK(mpiReadRaw(&refSignature.r, tv->r, tv->rLen));
        MPI_CHECK(mpiReadRaw(&refSignature.s, tv->s, tv->sLen));

        //Read pseudo-random number
        MPI_CHECK(mpiReadRaw(&r, tv->k, tv->kLen));

        end:
        //Any error to report?
        if(error)
        {
            //Test result
            TRACE_INFO("  Result:\r\n");
            TRACE_INFO("    FAILED\r\n\r\n");
            //Exit immediately
            break;
        }

        error = mpiSubInt(&t, &refPrivateKey.d, 1);

        //Any error to report?
        if(error)
        {
            //Test result
            TRACE_INFO("  Result:\r\n");
            TRACE_INFO("    FAILED\r\n\r\n");
            //Exit immediately
            break;
        }

        //Get the length, in bytes, of the random number
        n = (mpiGetByteLength(&t) + 3) / 4 * 4;

        memset(random, 0, sizeof(random));
        memcpy(random, t.data, n);

        //Initialize pseudo-random number generator
        prngSimulator.addEntropy(&prngContext, 0, random, n + 4, 0);

        //ECDSA key pair generation
        error = ecdsaGenerateKeyPair(PRNG_SIMULATOR, &prngContext, &params,
                                     &calcPrivateKey, &calcPublicKey);

        //Any error to report?
        if(error)
        {
            //Test result
            TRACE_INFO("  Result:\r\n");
            TRACE_INFO("    FAILED\r\n\r\n");
            //Exit immediately
            break;
        }

        //Dump calculated ECDSA private key
//        TRACE_DEBUG("  ECDSA private key:\r\n");
//        TRACE_DEBUG_MPI("    ", &calcPrivateKey.d);
//        TRACE_DEBUG("  ECDSA public key (X):\r\n");
//        TRACE_DEBUG_MPI("    ", &calcPublicKey.q.x);
//        TRACE_DEBUG("  ECDSA public key (Y):\r\n");
//        TRACE_DEBUG_MPI("    ", &calcPublicKey.q.y);

#if (BENCHMARK_TRNG_SUPPORT == ENABLED)
        //Do not check private key and public value
#else
        //Check resulting ECDSA key pair
        if(mpiComp(&calcPrivateKey.d, &refPrivateKey.d) ||
           mpiComp(&calcPublicKey.q.x, &refPublicKey.q.x) ||
           mpiComp(&calcPublicKey.q.y, &refPublicKey.q.y))
        {
            //Test result
            TRACE_INFO("  Result:\r\n");
            TRACE_INFO("    FAILED\r\n\r\n");
            //Report an error
            error = ERROR_FAILURE;
            //Exit immediately
            break;
        }
#endif

        error = mpiSubInt(&t, &r, 1);

        //Any error to report?
        if(error)
        {
            //Test result
            TRACE_INFO("  Result:\r\n");
            TRACE_INFO("    FAILED\r\n\r\n");
            //Exit immediately
            break;
        }

        //Get the length, in bytes, of the random number
        n = (mpiGetByteLength(&t) + 3) / 4 * 4;

        memset(random, 0, sizeof(random));
        memcpy(random, t.data, n);

        //Initialize pseudo-random number generator
        prngSimulator.addEntropy(&prngContext, 0, random, n + 4, 0);

        //Digest message
        error = tv->hashAlgo->compute(tv->m, tv->mLen, digest);

        //Any error to report?
        if(error)
        {
            //Test result
            TRACE_INFO("  Result:\r\n");
            TRACE_INFO("    FAILED\r\n\r\n");
            //Exit immediately
            break;
        }

#if (BENCHMARK_TRNG_SUPPORT == ENABLED)

      //ECDSA signature generation
      error = ecdsaGenerateSignature(PRNG_SIMULATOR, &prngContext, &params,
         &calcPrivateKey, digest, tv->hashAlgo->digestSize, &calcSignature);

#else
        //ECDSA signature generation
        error = ecdsaGenerateSignature(PRNG_SIMULATOR, &prngContext, &params,
                                       &refPrivateKey, digest, tv->hashAlgo->digestSize, &calcSignature);

#endif

        //Any error to report?
        if(error)
        {
            //Test result
            TRACE_INFO("  Result:\r\n");
            TRACE_INFO("    FAILED\r\n\r\n");
            //Exit immediately
            break;
        }

        //Dump calculated ECDSA signature
//        TRACE_DEBUG("  Calculated signature (r):\r\n");
//        TRACE_DEBUG_MPI("    ", &calcSignature.r);
//        TRACE_DEBUG("  Calculated signature (s):\r\n");
//        TRACE_DEBUG_MPI("    ", &calcSignature.s);

#if (BENCHMARK_TRNG_SUPPORT == ENABLED)
      //ECDSA signature verification
      error = ecdsaVerifySignature(&params, &calcPublicKey, digest,
         tv->hashAlgo->digestSize, &calcSignature);
#else
        //Check resulting ECDSA signature
        if(mpiComp(&calcSignature.r, &refSignature.r) || mpiComp(&calcSignature.s, &refSignature.s))
        {
            //Test result
            TRACE_INFO("  Result:\r\n");
            TRACE_INFO("    FAILED\r\n\r\n");
            //Report an error
            error = ERROR_FAILURE;
            //Exit immediately
            break;
        }

#endif
        //Any error to report?
        if(error)
        {
            //Test result
            TRACE_INFO("  Result:\r\n");
            TRACE_INFO("    FAILED\r\n\r\n");
            //Exit immediately
            break;
        }

        ecFreeDomainParameters(&params);

        //Test result
        TRACE_INFO("  Result:\r\n");
        TRACE_INFO("    PASSED\r\n\r\n");
    }

    //Release previously allocated resources
    ecFreeDomainParameters(&params);
    ecFreePrivateKey(&refPrivateKey);
    ecFreePrivateKey(&calcPrivateKey);
    ecFreePublicKey(&refPublicKey);
    ecFreePublicKey(&calcPublicKey);
    ecdsaFreeSignature(&refSignature);
    ecdsaFreeSignature(&calcSignature);
    mpiFree(&r);
    mpiFree(&t);

    //Debug message
    TRACE_INFO("\r\n");

    //Return status code
    return error;
}